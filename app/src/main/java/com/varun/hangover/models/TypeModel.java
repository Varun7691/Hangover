public class TypeModel {

    private String typeId;
    private String typeName;
    private String typeAbout;
    private String typeLikes;

    public String getTypeId() {
        return typeId;
    }

    public void setTypeId(String typeId) {
        this.typeId = typeId;
    }

    public String getTypeName() {
        return typeName;
    }

    public void setTypeName(String typeName) {
        this.typeName = typeName;
    }

    public String getTypeAbout() {
        return typeAbout;
    }

    public void setTypeAbout(String typeAbout) {
        this.typeAbout = typeAbout;
    }

    public String getTypeLikes() {
        return typeLikes;
    }

    public void setTypeLikes(String typeLikes) {
        this.typeLikes = typeLikes;
    }
}
