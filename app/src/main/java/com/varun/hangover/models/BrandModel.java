public class BrandModel {

    private String brandId;
    private String brandName;
    private String brandAbout;
    private String brandLogo;
    private String brandLikes;

    public String getBrandId() {
        return brandId;
    }

    public void setBrandId(String brandId) {
        this.brandId = brandId;
    }

    public String getBrandName() {
        return brandName;
    }

    public void setBrandName(String brandName) {
        this.brandName = brandName;
    }

    public String getBrandAbout() {
        return brandAbout;
    }

    public void setBrandAbout(String brandAbout) {
        this.brandAbout = brandAbout;
    }

    public String getBrandLogo() {
        return brandLogo;
    }

    public void setBrandLogo(String brandLogo) {
        this.brandLogo = brandLogo;
    }

    public String getBrandLikes() {
        return brandLikes;
    }

    public void setBrandLikes(String brandLikes) {
        this.brandLikes = brandLikes;
    }
}
